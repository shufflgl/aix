import fs from "fs-extra";
import os from "node:os";
import path from "node:path";
import { copyAndBuildMcp, copySkills, defaultNonSecretEnv, envExample, launcherScript, timestamp } from "../core/build.js";
import { ExtensionManifest } from "../core/manifest.js";
import { expandHome, workspaceRoot } from "../core/paths.js";
import { setInstalled, removeInstalled } from "../core/state.js";
import { commandExists, runCommand } from "../utils/shell.js";

export function claudeCodeLocalPluginPath(extensionId: string): string {
  return path.join(os.homedir(), ".claude", "plugins", "local", extensionId);
}

export async function buildClaudeCodePlugin(manifest: ExtensionManifest): Promise<string> {
  const root = workspaceRoot();
  const platformPlugin = path.join(root, "platforms", "claude-code", "plugins", manifest.id);
  await fs.remove(platformPlugin);
  await copyAndBuildMcp(manifest, path.join(platformPlugin, "server"));
  await copySkills(manifest, path.join(platformPlugin, "skills"));
  await fs.ensureDir(path.join(platformPlugin, "scripts"));
  await fs.writeFile(path.join(platformPlugin, "scripts", `start-${manifest.id}.mjs`), launcherScript());
  await fs.chmod(path.join(platformPlugin, "scripts", `start-${manifest.id}.mjs`), 0o755);
  await fs.writeJson(path.join(platformPlugin, "plugin.json"), claudeCodePluginJson(manifest), { spaces: 2 });
  await fs.writeJson(path.join(platformPlugin, ".mcp.json"), claudeCodeMcpJson(manifest), { spaces: 2 });
  await fs.writeFile(path.join(platformPlugin, ".env.example"), envExample(manifest));
  await fs.writeFile(path.join(platformPlugin, "README.md"), `# ${manifest.name}\n\nClaude Code plugin generated by aix.\n`);
  return platformPlugin;
}

export async function installClaudeCodePlugin(manifest: ExtensionManifest): Promise<string> {
  const builtPath = await buildClaudeCodePlugin(manifest);
  const installPath = expandHome(manifest.targets.claudeCode?.installPath ?? claudeCodeLocalPluginPath(manifest.id));
  await fs.remove(installPath);
  await fs.ensureDir(path.dirname(installPath));
  await fs.copy(builtPath, installPath);

  if (await commandExists("claude")) {
    await runCommand(`claude plugin add ${installPath}`, workspaceRoot()).catch(() => undefined);
  }

  await setInstalled(manifest.id, "claude-code", {
    version: manifest.version,
    installedPath: installPath,
    installedAt: new Date().toISOString()
  });
  return installPath;
}

export async function removeClaudeCodePlugin(extensionId: string): Promise<void> {
  await fs.remove(claudeCodeLocalPluginPath(extensionId));
  await removeInstalled(extensionId, "claude-code");
}

function claudeCodePluginJson(manifest: ExtensionManifest) {
  return {
    name: manifest.id,
    version: `${manifest.version}+claude-code.${timestamp()}`,
    description: manifest.description,
    author: { name: "Local developer" },
    homepage: manifest.homepage,
    repository: manifest.repository,
    license: manifest.license,
    keywords: ["mcp", "skill", manifest.id],
    mcpServers: "./.mcp.json",
    skills: "./skills/"
  };
}

function claudeCodeMcpJson(manifest: ExtensionManifest) {
  return {
    mcpServers: {
      [manifest.mcp.id]: {
        command: "node",
        args: [`./scripts/start-${manifest.id}.mjs`],
        cwd: ".",
        env: defaultNonSecretEnv(manifest)
      }
    }
  };
}
